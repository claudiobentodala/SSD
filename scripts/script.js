function mudouTamanho(){
    if(window.innerWidth >= 699 ){
        itens.style.display = 'flex'
    } 
    else{
        itens.style.display = 'flex'
    }
}
function clickMenu() {
     if (itens.style.display == 'none') {
         itens.style.display = 'flex'
    } 
    else{
        itens.style.display = 'none'
    }
}